#!/bin/bash

# Скрипт быстрого обновления бота #2 (Tour Booking Bot) на VPS
# Инструкция:
# 1. Выполните: chmod +x bot2_update.sh
# 2. Запустите: ./bot2_update.sh

echo "---------------------------------------------------"
echo "♻️  Обновление BOT #2 (Tour Booking)..."
echo "---------------------------------------------------"

# 1. Подтягиваем изменения из Git
echo "⬇️  Git pull..."
git pull

# 2. Установка новых зависимостей (если появились)
echo "📦 Проверка библиотек бота..."
cd bot
npm install
cd ..

# 3. Перезапуск процесса в PM2
echo "🎬 Перезапуск процесса bot2..."
pm2 restart bot2

echo ""
echo "---------------------------------------------------"
echo "✅ BOT #2 ОБНОВЛЕН И ПЕРЕЗАПУЩЕН!"
echo "---------------------------------------------------"
