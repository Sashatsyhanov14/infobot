-- ==========================================
-- ПОЛНЫЙ СКРИПТ НАСТРОЙКИ SUPABASE (Excursion Bot)
-- ==========================================

-- 1. ТАБЛИЦА ПОЛЬЗОВАТЕЛЕЙ
CREATE TABLE IF NOT EXISTS users (
    telegram_id BIGINT PRIMARY KEY,
    username TEXT,
    role TEXT DEFAULT 'user', -- 'user', 'manager', 'founder'
    referrer_id BIGINT REFERENCES users(telegram_id),
    balance DECIMAL(10, 2) DEFAULT 0.00,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. ТАБЛИЦА ЭКСКУРСИЙ
CREATE TABLE IF NOT EXISTS excursions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    sort_number INTEGER DEFAULT 1,
    city TEXT NOT NULL,
    title TEXT NOT NULL,
    description TEXT,
    price_rub INTEGER NOT NULL,
    duration TEXT,
    included TEXT,
    meeting_point TEXT,
    image_url TEXT,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 3. ТАБЛИЦА ЗАЯВОК (Requests)
CREATE TABLE IF NOT EXISTS requests (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id BIGINT REFERENCES users(telegram_id),
    excursion_id UUID REFERENCES excursions(id),
    excursion_title TEXT, -- Сохраняем на случай удаления экскурсии
    full_name TEXT,
    tour_date TEXT,
    hotel_name TEXT,
    price_rub INTEGER,
    status TEXT DEFAULT 'new', -- 'new', 'contacted', 'done', 'cancelled'
    assigned_manager BIGINT REFERENCES users(telegram_id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 4. ИСТОРИЯ ЧАТА (для AI)
CREATE TABLE IF NOT EXISTS chat_history (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id BIGINT REFERENCES users(telegram_id),
    role TEXT NOT NULL, -- 'user', 'assistant'
    content TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 5. FAQ (База знаний AI)
CREATE TABLE IF NOT EXISTS faq (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    topic TEXT NOT NULL,
    content_ru TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

