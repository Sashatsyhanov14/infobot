#!/bin/bash

# Скрипт быстрого обновления Tour Booking Bot на VPS
# Инструкция:
# 1. Выполните: chmod +x vps_update.sh
# 2. Запустите: ./vps_update.sh

echo "---------------------------------------------------"
echo "♻️  Обновление Tour Booking Bot (bot2)..."
echo "---------------------------------------------------"

# 1. Подтягиваем изменения из Git
echo "⬇️  Git pull..."
git pull

# 2. Установка новых зависимостей (если появились)
echo "📦 Проверка зависимостей..."
cd bot
npm install
cd ..

# 3. Перезапуск процесса в PM2
echo "🎬 Перезапуск процесса bot2..."
pm2 restart bot2

echo ""
echo "---------------------------------------------------"
echo "✅ ОБНОВЛЕНИЕ ЗАВЕРШЕНО!"
echo "Бот перезапущен. Команда для логов: pm2 logs bot2"
echo "---------------------------------------------------"
